export function exerciseImages(exId, variant = false) {
  const m = EXERCISE_MEDIA[exId]; if (!m) return [];
  const slot = variant ? m.variant : m.main;
  return (slot && slot.urls) ? slot.urls : [];
}
export function exerciseSource(exId, variant = false) {
  const m = EXERCISE_MEDIA[exId]; if (!m) return "";
  const slot = variant ? m.variant : m.main;
  return (slot && slot.source) ? slot.source : "";
}
export const EXERCISE_INDEX = (() => {
  const idx = {};
  for (const day of DAYS) for (const ex of day.exercises) idx[ex.id] = { ...ex, dayId: day.id, dayName: day.name };
  return idx;
})();
